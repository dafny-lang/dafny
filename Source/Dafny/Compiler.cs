    protected virtual TargetWriter EmitIf(out TargetWriter guardWriter, bool hasElse, TargetWriter wr) {
      wr.Indent();
      wr.Write("if (");
      guardWriter = new TargetWriter(wr.IndentLevel);
      wr.Append(guardWriter);
      var thn = wr.NewBlock(")");
      if (hasElse) {
