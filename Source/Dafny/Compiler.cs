    /// <summary>
    /// Returns "true" if "type" denotes a type for which a specific compiled value (non-ghost witness) is known.
    /// </summary>
    public static bool InitializerIsKnown(Type type) {
      Contract.Requires(type != null);

      bool hs, hz, ik;
      string dv;
